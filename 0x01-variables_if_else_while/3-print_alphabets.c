#include <stdio.h>

/**
 * main - Entry point
 *
 *
 *Return: Always 0 (succeed)
 **/
int main(void)
{
	char letters;
	char LETTERS;

	letters = 'a';
	LETTERS = 'A';
	while
		(letters <= 'z')
		{
		       putchar(letters);
		       letters++;
		}
	while
		(LETTERS <= 'z')
		{
			putchar(LETTERS);
			LETTERS++;
		}
	return (0);
}
